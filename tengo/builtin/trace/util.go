package trace

import (
	"github.com/olive-io/bee/tengo/slog"
)

func parseLevel(text string) (slog.Level, bool) {
	var level slog.Level
	switch text {
	case "debug":
		level = slog.LevelDebug
	case "info":
		level = slog.LevelInfo
	case "warn":
		level = slog.LevelWarn
	case "error":
		level = slog.LevelError
	default:
		return 0, false
	}
	return level, true
}
